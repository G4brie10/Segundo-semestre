    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String u,p,t;
            u = txtUsuario.getText();
            t = comboTipo.getSelectedItem().toString();
            p = password.getText();
            String user = "root";
            String url = "jdbc:mysql://localhost:3306/";
            String password = "123456";
            String bd = "practicaosvaldo";
            String con = "com.mysql.cj.jdbc.Driver";
            Class.forName(con);
            Connection co = DriverManager.getConnection(url+bd,user,password);
            //JOptionPane.showMessageDialog(null, "si se conecta");
            String query = "SELECT * FROM usuarios WHERE Usuarios='"+u+"' and " + "Password='"+p+"'";
            Statement sql = co.createStatement();
            ResultSet datos = sql.executeQuery(query);
            if(datos.next()){
                JOptionPane.showMessageDialog(null,"si esta en base de datos");
                String tipo = datos.getString(3);
                //JOptionPane.showMessageDialog(null, tipo);
                MDIPrincipal pr = new MDIPrincipal();
                if(tipo.equals("Administrador")){
                    pr.mnuAAgregar.setEnabled(true);
                    pr.mnuAeliminar.setEnabled(true);
                    pr.mnuAModificar.setEnabled(true);
                    pr.jMenuItem1.setText(u);
                }
                pr.jMenuItem1.setText(u);
                pr.setVisible(true);
                this.hide();
            }else{
                JOptionPane.showMessageDialog(null,"no esta en base de datos");
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

